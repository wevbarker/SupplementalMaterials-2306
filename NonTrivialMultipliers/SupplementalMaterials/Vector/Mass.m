Comment@{"Note that",Cref@{"VectorSectionyyEqn2j","VectorSectionyyEqn3j"}," allow us to solve for",Inline@T3[-m]," and",Inline@T2[m]," purely in terms of the 2-form."};

Comment@{"And here is the tensor equation",Cref@"VectorSectionyyEqn1","."};
